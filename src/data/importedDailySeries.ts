export function getImportedDailyPoint(city: City, metric: Metric, dateIso: string): { plan: number; fact: number } | null {
  const series = importedDailySeries[city];

  if (!series || dateIso < series.startDate || dateIso > series.endDate) {
    return null;
  }

  const dayMs = 24 * 60 * 60 * 1000;
  const offset = Math.round((Date.parse(`${dateIso}T00:00:00Z`) - Date.parse(`${series.startDate}T00:00:00Z`)) / dayMs);
  const metricSeries = series.metrics[metric];

  return {
    plan: metricSeries.plan[offset] ?? 0,
    fact: metricSeries.fact[offset] ?? 0,
  };
}
